    public static void showTaiKhoan(List<TaiKhoan> list) {
        DefaultTableModel dtm = (DefaultTableModel) jTable_TaiKhoan.getModel();
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        Object[] os = new Object[4];
        for (int i = 0; i < list.size(); i++) {
            os[0] = i + 1;
            os[1] = list.get(i).getMaTK();
            os[2] = list.get(i).getTenTK();
            os[3] = list.get(i).getMatKhau();
            dtm.addRow(os);
        }
    }

