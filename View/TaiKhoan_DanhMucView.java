    private void jButton_TimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_TimKiemActionPerformed
        // TODO add your handling code here:
        // Xoa table
        DefaultTableModel dtm = (DefaultTableModel) jTable_TaiKhoan.getModel();
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }

        String tenTK = jTextField_TenTK.getText();
        String matKhau = jTextField_MatKhau.getText();

        if (tenTK.equals("") && matKhau.equals("")) {
            JOptionPane.showMessageDialog(null, "Hãy nhập một điều kiện tìm kiếm!!!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        } else {
            List<TaiKhoan> listTK = new ArrayList<>();
            try {
                String connectionUrl = "jdbc:sqlserver://localhost:1433;DatabaseName=DBComputerStore;user=sa;password=123456;encrypt=true;trustServerCertificate=true";
                Connection con = DriverManager.getConnection(connectionUrl);
                String sql = "SELECT * FROM tblLoginSys WHERE 1 = 1";
                if (!tenTK.equals("")) {
                    sql = sql + " AND TenTK LIKE '%" + tenTK + "%'";
                }
                if (!matKhau.equals("")) {
                    sql = sql + " AND MatKhau LIKE '%" + matKhau + "%'";
                }
                PreparedStatement pst = con.prepareStatement(sql);
                ResultSet rs = pst.executeQuery();
                TaiKhoan tk;
                while (rs.next()) {
                    tk = new TaiKhoan(rs.getString(1), rs.getString(2), rs.getString(3));
                    listTK.add(tk);
                }
                pst.close();
                con.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }

            Object[] os = new Object[4];
            int dem = 1;
            for (int i = 0; i < listTK.size(); i++) {
                os[0] = dem++;
                os[1] = listTK.get(i).getMaTK();
                os[2] = listTK.get(i).getTenTK();
                os[3] = listTK.get(i).getMatKhau();
                dtm.addRow(os);
            }
            if (dem == 1) {
                JOptionPane.showMessageDialog(null, "Không có tài khoản thỏa mãn điều kiện!", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton_TimKiemActionPerformed

