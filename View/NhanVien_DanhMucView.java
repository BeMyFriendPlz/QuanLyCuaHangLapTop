    private void jButton_TimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_TimKiemActionPerformed
        // TODO add your handling code here:
        // Xoa table
        DefaultTableModel dtm = (DefaultTableModel) jTable_NhanVien.getModel();
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        
        String tenNV = jTextField_TenNV.getText();
        String gioiTinh = "";
        if (jRadioButton_Nam.isSelected()) {
            gioiTinh = "Nam";
        }
        if (jRadioButton_Nu.isSelected()) {
            gioiTinh = "Nữ";
        }
        String ngaySinh = "";
        if(!jTextField_NgaySinh.getText().toString().equals("")) {
            try {
                ngaySinh = format.format(sdf.parse(jTextField_NgaySinh.getText()));
            } catch (ParseException ex) {
                Logger.getLogger(NhanVien_DanhMucView.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        String diaChi = jTextField_DiaChi.getText();
        String sdt = jTextField_SDT.getText();
        String luong = jTextField_Luong.getText();

        if (tenNV.equals("") && gioiTinh.equals("") && ngaySinh.equals("") && diaChi.equals("") && sdt.equals("") && luong.equals("")) {
            JOptionPane.showMessageDialog(null, "Hãy nhập một điều kiện tìm kiếm!!!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        } else {
            List<NhanVien> listNV = new ArrayList<>();
            try {
                String connectionUrl = "jdbc:sqlserver://localhost:1433;DatabaseName=DBComputerStore;user=sa;password=123456;encrypt=true;trustServerCertificate=true";
                Connection con = DriverManager.getConnection(connectionUrl);
                String sql = "SELECT * FROM tblNhanVien WHERE 1 = 1";
                if (!tenNV.equals("")) {
                    sql = sql + " AND TenNV LIKE N'%" + tenNV + "%'";
                }
                if (!gioiTinh.equals("")) {
                    sql = sql + " AND GioiTinh LIKE N'%" + gioiTinh + "%'";
                }
                if (!ngaySinh.equals("")) {
                    sql = sql + " AND NgaySinh = '" + ngaySinh + "'";
                }
                if (!diaChi.equals("")) {
                    sql = sql + " AND DiaChi LIKE N'%" + diaChi + "%'";
                }
                if (!sdt.equals("")) {
                    sql = sql + " AND DienThoai LIKE '%" + sdt + "%'";
                }
                if (!luong.equals("")) {
                    sql = sql + " AND Luong <= " + luong;
                }
                PreparedStatement pst = con.prepareStatement(sql);
                ResultSet rs = pst.executeQuery();
                NhanVien nv;
                while (rs.next()) {
                    nv = new NhanVien(rs.getString(1), rs.getString(2), rs.getString(3), rs.getDate(4), rs.getString(5), rs.getString(6), rs.getLong(7));
                    listNV.add(nv);
                }
                pst.close();
                con.close();
            } catch (SQLException ex) {
                ex.printStackTrace();
            }

            Object[] os = new Object[8];
            int dem = 1;
            for (int i = 0; i < listNV.size(); i++) {
                if (listNV.get(i).getMaNV().equals("Admin")) {
                    continue;
                }
                os[0] = dem++;
                os[1] = listNV.get(i).getMaNV();
                os[2] = listNV.get(i).getTenNV();
                os[3] = listNV.get(i).getGioiTinh();
                os[4] = sdf.format(listNV.get(i).getNgaySinh());
                os[5] = listNV.get(i).getDiaChi();
                os[6] = listNV.get(i).getDienThoai();
                os[7] = listNV.get(i).getLuong();
                dtm.addRow(os);
            }
            if (dem == 1) {
                JOptionPane.showMessageDialog(null, "Không có nhân viên thỏa mãn điều kiện!", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton_TimKiemActionPerformed

