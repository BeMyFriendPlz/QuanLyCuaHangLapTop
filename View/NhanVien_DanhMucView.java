    public static void showNhanVien(List<NhanVien> list) {
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        DefaultTableModel dtm = (DefaultTableModel) jTable_NhanVien.getModel();
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        Object[] os = new Object[8];
        for (int i = 1; i < list.size(); i++) {
            os[0] = i;
            os[1] = list.get(i).getMaNV();
            os[2] = list.get(i).getTenNV();
            os[3] = list.get(i).getGioiTinh();
            os[4] = sdf.format(list.get(i).getNgaySinh());
            os[5] = list.get(i).getDiaChi();
            os[6] = list.get(i).getDienThoai();
            os[7] = list.get(i).getLuong();
            dtm.addRow(os);
        }
    }

