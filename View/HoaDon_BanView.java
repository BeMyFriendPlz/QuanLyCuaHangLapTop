    private void jButton_ThemSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ThemSPActionPerformed
        // TODO add your handling code here:
        if(jComboBox_MaSP.getSelectedItem().equals("") || jTextField_SoLuong.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập lại dữ liệu thích hợp!", "Dữ liệu không hợp lệ", JOptionPane.ERROR_MESSAGE);
        } else {
            String maSP = jComboBox_MaSP.getSelectedItem().toString();
            int SL = Integer.valueOf(jTextField_SoLuong.getText());
            if(SL <= SanPham.getSoLuong(maSP)) {
                DefaultTableModel dtm = (DefaultTableModel) jTable_DanhSachSP.getModel();
                Object[] os = new Object[7];
                os[0] = dtm.getRowCount() + 1;
                os[1] = jComboBox_MaSP.getSelectedItem();
                os[2] = jTextField_TenSP.getText();
                os[3] = jTextField_SoLuong.getText();
                os[4] = jTextField_DonGia.getText();
                os[5] = jTextField_GiamGia.getText();
                os[6] = jTextField_ThanhTien.getText();
                dtm.addRow(os);
                // Tinh tong tien
                long tongTien = Long.valueOf(jTextField_ThanhTien.getText()) + Long.valueOf(jTextField_TongTien.getText());
                jTextField_TongTien.setText(String.valueOf(tongTien));
                JOptionPane.showMessageDialog(null, "Thêm sản phẩm thành công!", "Thành công", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Vui lòng kiểm tra lại số lượng sản phẩm!", "Sản phẩm không còn đủ!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton_ThemSPActionPerformed

