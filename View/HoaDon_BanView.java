    private void showJTable() {
        // Them khach hang
        List<KhachHang> khachList = KhachHang.khachList();
        jComboBox_MaKH.addItem("");
        for (int i = 0; i < khachList.size(); i++) {
            jComboBox_MaKH.addItem(khachList.get(i).getMaKH());
        }
        // Them san pham
        List<SanPham> sanPhamList = SanPham.sanPhamList();
        jComboBox_MaSP.addItem("");
        for (int i = 0; i < sanPhamList.size(); i++) {
            jComboBox_MaSP.addItem(sanPhamList.get(i).getMaSP());
        }
    }

    private void showHoaDon() {
        // Hien thi ma hoa don va ngay tao hoa don
        List<HoaDon> hoaDonList = HoaDon.hoaDonList();
        String maHD;
        if(hoaDonList.isEmpty()) {
            maHD = "001";
        } else {
            String ma = hoaDonList.get(hoaDonList.size()-1).getMaHD();
            int soLuong = Integer.valueOf(ma.substring(3,ma.length())) + 1;
            maHD = String.valueOf(soLuong);
            while (maHD.length() < 3) maHD = "0" + maHD;
        }
        jTextField_MaHD.setText("HD" + maHD);
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        Date ngayBan = new Date();
        jTextField_NgayBan.setText(sdf.format(ngayBan));
    }

    private void showTaiKhoan() {
        List<NhanVien> nhanVienList = NhanVien.nhanVienList();
        TaiKhoan tk = TaiKhoan.layTaiKhoan(tenTK);
        jTextField_MaTK.setText(tk.getMaTK());
        String tenTK = tk.getTenTK();
        for (int i = 0; i < nhanVienList.size(); i++) {
            String maNV = nhanVienList.get(i).getMaNV();
            if(maNV.equals(tenTK)) {
                jTextField_TenNV.setText(nhanVienList.get(i).getTenNV());
                break;
            }
        }
    }
