    private void jComboBox_MaKHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox_MaKHActionPerformed
        // TODO add your handling code here:
        List<KhachHang> khachList = KhachHang.khachList();
        String maKH = (String) jComboBox_MaKH.getSelectedItem();
        if(maKH.equals("")) {
            jTextField_TenKH.setText("");
            jTextField_DiaChi.setText("");
            jTextField_SDT.setText("");
        } else {
            for (int i = 0; i < khachList.size(); i++) {
                String ma = khachList.get(i).getMaKH();
                if(ma.equals(maKH)) {
                    jTextField_TenKH.setText(khachList.get(i).getTenKH());
                    jTextField_DiaChi.setText(khachList.get(i).getDiaChi());
                    jTextField_SDT.setText(khachList.get(i).getDienThoai());
                    break;
                }
            }
        }
    }//GEN-LAST:event_jComboBox_MaKHActionPerformed

    private void jComboBox_MaSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox_MaSPActionPerformed
        // TODO add your handling code here:
        List<SanPham> sanPhamList = SanPham.sanPhamList();
        String maSP = (String) jComboBox_MaSP.getSelectedItem();
        if(maSP.equals("")) {
            jTextField_TenSP.setText("");
            jTextField_DonGia.setText("");
            jTextField_SoLuong.setText("");
            jTextField_GiamGia.setText("0");
            jTextField_ThanhTien.setText("0");
        } else {
            for (int i = 0; i < sanPhamList.size(); i++) {
                String ma = sanPhamList.get(i).getMaSP();
                if(ma.equals(maSP)) {
                    jTextField_TenSP.setText(sanPhamList.get(i).getTenSP());
                    jTextField_DonGia.setText(String.valueOf(sanPhamList.get(i).getGiaBan()));
                    jTextField_SoLuong.setText("1");
                    jTextField_GiamGia.setText("0");
                    jTextField_ThanhTien.setText(String.valueOf(sanPhamList.get(i).getGiaBan()));
                    break;
                }
            }
        }
    }//GEN-LAST:event_jComboBox_MaSPActionPerformed

    private void jTextField_SoLuongFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField_SoLuongFocusLost
        // TODO add your handling code here:
        int soLuong = Integer.valueOf(jTextField_SoLuong.getText());
        int giamGia = Integer.valueOf(jTextField_GiamGia.getText());
        if(soLuong <= 0) {
            jTextField_ThanhTien.setText("0");
            JOptionPane.showMessageDialog(null, "Vui lòng nhập lại dữ liệu thích hợp!", "Dữ liệu không hợp lệ", JOptionPane.ERROR_MESSAGE);
        } else if(giamGia >= 0) {
            long donBan = Long.valueOf(jTextField_DonGia.getText());
            Long thanhTien = soLuong * donBan * (100 - giamGia) / 100;
            jTextField_ThanhTien.setText(String.valueOf(thanhTien));
        }
    }//GEN-LAST:event_jTextField_SoLuongFocusLost

