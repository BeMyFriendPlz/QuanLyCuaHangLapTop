    private void jButton_LuuHDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LuuHDActionPerformed
        if(jComboBox_MaKH.getSelectedItem().toString().equals("") || jTable_DanhSachSP.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Thông tin hóa đơn chưa đầy đủ!", "Lỗi dữ liệu", JOptionPane.ERROR_MESSAGE);
        } else {
            String maHD = jTextField_MaHD.getText();

            // Nhap hoa don
            try {
                String maTK = jTextField_MaTK.getText();
                String maKH = jComboBox_MaKH.getSelectedItem().toString();
                Date ngayBan = new SimpleDateFormat("dd/MM/yyyy").parse(jTextField_NgayBan.getText());
                long tongTien = Long.valueOf(jTextField_TongTien.getText());
                HoaDon hd = new HoaDon(maHD, maTK, maKH, ngayBan, tongTien);
                hd.nhapHoaDon();
            } catch (ParseException ex) {
                Logger.getLogger(HoaDon_BanView.class.getName()).log(Level.SEVERE, null, ex);
            }

            // Nhap chi tiet hoa don
            DefaultTableModel dtm = (DefaultTableModel) jTable_DanhSachSP.getModel();
            for (int i = 0; i < jTable_DanhSachSP.getRowCount(); i++) {
                String maSP = dtm.getValueAt(i, 1).toString();
                int soLuong = Integer.valueOf(dtm.getValueAt(i, 3).toString());
                long donGia = Long.valueOf(dtm.getValueAt(i, 4).toString());
                int giamGia = Integer.valueOf(dtm.getValueAt(i, 5).toString());
                long thanhTien = Long.valueOf(dtm.getValueAt(i, 6).toString());
                ChiTietHoaDon cthd = new ChiTietHoaDon(maHD, maSP, soLuong, donGia, giamGia, thanhTien);
                cthd.nhapChiTietHoaDon();
                
                // Sua so luong san pham
                SanPham.suaSoLuongSP(maSP, soLuong);
            }
            
            JOptionPane.showMessageDialog(null, "Lưu hóa đơn thành công!", "Thành công!", JOptionPane.INFORMATION_MESSAGE);
            jButton_InHD.setEnabled(true);
        }
        
    }//GEN-LAST:event_jButton_LuuHDActionPerformed

