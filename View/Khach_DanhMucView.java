    public static void showKhach(ArrayList<KhachHang> list) {
        DefaultTableModel dtm = (DefaultTableModel) jTable_KhachHang.getModel();
        while (dtm.getRowCount() > 0) {            
            dtm.removeRow(0);
        }
        Object[] os = new Object[5];
        for (int i = 0; i < list.size(); i++) {
            os[0] = i+1;
            os[1] = list.get(i).getMaKH();
            os[2] = list.get(i).getTenKH();
            os[3] = list.get(i).getDiaChi();
            os[4] = list.get(i).getDienThoai();
            dtm.addRow(os);
        }
    }

