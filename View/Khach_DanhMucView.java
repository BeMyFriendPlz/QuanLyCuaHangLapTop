    private void jButton_XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_XoaActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel) jTable_KhachHang.getModel();
        if(jTable_KhachHang.getSelectedRowCount() == 1) {
            int selectedRow = jTable_KhachHang.getSelectedRow();
            String maKH = dtm.getValueAt(selectedRow, 1).toString();
            if(selectedRow < listSize) listSize--;
            maXoa.add(maKH);
            for (int i = 0; i < list.size(); i++) {
                if(list.get(i).getMaKH().equals(maKH)) {
                    list.remove(i);
                    break;
                }
            }
            showKhach(list);
            jButton_Luu.setEnabled(true);
            JOptionPane.showMessageDialog(null, "Đã xóa khách hàng thành công!", "Thực hiện thành công", JOptionPane.INFORMATION_MESSAGE);
        } else {
            if(jTable_KhachHang.getRowCount() == 0) {
                JOptionPane.showMessageDialog(null, "Bảng không có dữ liệu!", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Hay chọn từng cột để xóa!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        jTextField_MaKH.setText("");
        jTextField_TenKH.setText("");
        jTextField_DiaChiKH.setText("");
        jTextField_SDTKH.setText("");
    }//GEN-LAST:event_jButton_XoaActionPerformed

